%对真实表达数据进行离散化:将整个矩阵中数据的取值【0，1】平均分成20段
%A中每个数据所属的种类，所得矩阵放在C中,这是对矩阵A的比较真实的刻画和反映
function C=Discrete_realdata(A)
%%A=[0.2 0.3 0.11;0.2 0.3 0.33; 0.1 0.5 0.6]
C1=1*(A<0.05);
C2=2*(A>=0.05).*(A<0.1);
C3=3*(A>=0.1).*(A<0.15);
C4=4*(A>=0.15).*(A<0.2);
C5=5*(A>=0.2).*(A<0.25);
C6=6*(A>=0.25).*(A<0.3);
C7=7*(A>=0.3).*(A<0.35);
C8=8*(A>=0.35).*(A<0.4);
C9=9*(A>=0.4).*(A<0.45);
C10=10*(A>=0.45).*(A<0.5);
C11=11*(A>=0.5).*(A<0.55);
C12=12*(A>=0.55).*(A<0.6);
C13=13*(A>=0.6).*(A<0.65);
C14=14*(A>=0.65).*(A<0.7);

C15=15*(A>=0.7).*(A<0.75);
C16=16*(A>=0.75).*(A<0.8);
C17=17*(A>=0.8).*(A<0.85);
C18=18*(A>=0.85).*(A<0.9);
C19=19*(A>=0.9).*(A<0.95);
C20=20*(A>=0.95).*(A<=1);
C=C1+C2+C3+C4+C5+C6+C7+C8+C9+C10+C11+C12+C13+C14+C15+C16+C17+C18+C19+C20;

